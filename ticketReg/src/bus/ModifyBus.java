    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index=jTable1.getSelectedRow();
        TableModel model=jTable1.getModel();

        bid = Integer.parseInt(model.getValueAt(index,0).toString());
        try{
            dbconnect db=new dbconnect();
            db.connect();
            String sql="select * from bus_Info where bid="+bid+"";
            db.pst= db.con.prepareStatement(sql);
            
            db.rs=db.pst.executeQuery();

            if(db.rs.next())
            {
             
               jTextField1.setText(db.rs.getString("bname"));
                jTextField2.setText(db.rs.getString("bsource"));
                jTextField3.setText(db.rs.getString("bdestination"));
                jTextField4.setText(db.rs.getString("bstart_time"));
                jTextField5.setText(db.rs.getString("bend_time"));
                jTextField7.setText(db.rs.getString("price"));
                jTextField8.setText(db.rs.getString("seats"));

            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked


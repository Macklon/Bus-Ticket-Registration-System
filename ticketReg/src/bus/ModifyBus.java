    private void bus_info(){
    try{
        dbconnect db=new dbconnect();
        db.connect();
        String sql="select * from bus_info";
        db.pst= db.con.prepareStatement(sql);
        db.rs=db.pst.executeQuery();
        
        while(db.rs.next()){   
        
        DefaultTableModel model1 = (DefaultTableModel)jTable1.getModel();
        jTable1.getColumnModel().getColumn(0).setWidth(0);
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        model1.addRow(new Object[]{db.rs.getInt(1),db.rs.getString(2),db.rs.getString(3),db.rs.getString(4),db.rs.getString(5),db.rs.getString(6),db.rs.getString(7),db.rs.getString(8)});
        }
    
    }
    catch(Exception e){
    JOptionPane.showMessageDialog(null, e);
     }
    }
    
     private void updateTable(){
        try{
            dbconnect db=new dbconnect();
            db.connect();
            String sql="select * from bus_info";   
            db.pst= db.con.prepareStatement(sql);
            db.rs=db.pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(db.rs));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            dbconnect db=new dbconnect();
            db.connect();
            String sql="update bus_info set bname=?,bsource=?,bdestination=?,bstart_time=?,bend_time=?,price=?,seats=? where bid="+bid+"";
            db.pst= db.con.prepareStatement(sql);
            db.pst.setString(1,jTextField1.getText());
            db.pst.setString(2,jTextField2.getText());
            db.pst.setString(3,jTextField3.getText());
            db.pst.setString(4,jTextField4.getText());
            db.pst.setString(5,jTextField5.getText());
            db.pst.setInt(6,Integer.parseInt(jTextField7.getText()));
            db.pst.setInt(7,Integer.parseInt(jTextField8.getText()));

            if(db.pst.executeUpdate() != 0){
                JOptionPane.showMessageDialog(null, "Bus Modified successfully");
            }else{
                JOptionPane.showMessageDialog(null, "Error: Check Your Information");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        updateTable();
    }//GEN-LAST:event_jButton1ActionPerformed


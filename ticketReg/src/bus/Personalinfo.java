    private void fillText(){
        
        lbd.setText(Login.bsource+" to "+Login.bdestination);
        lpdate.setText(Login.date);
        
        try{
            db.connect();
            String sql="select * from user_info where uid=?";
            db.pst= db.con.prepareStatement(sql);
            db.pst.setInt(1,Login.uid);
            db.rs=db.pst.executeQuery();
           
            while(db.rs.next()){
                temail.setText(db.rs.getString(3));
                tphno.setText(db.rs.getString(2));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    

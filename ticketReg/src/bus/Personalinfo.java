    private void bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookActionPerformed
        // TODO add your handling code here:
        try{
            dbconnect db=new dbconnect();
            db.connect();
            String sql="insert into ticket(bid,uid,bd_id,phno,email,name,age,gender,seats,price,date)values(?,?,?,?,?,?,?,?,?,?,?)";   
            db.pst= db.con.prepareStatement(sql);
            db.pst.setInt(1,Login.bid);
            db.pst.setInt(2,Login.uid);
            db.pst.setInt(3,Login.bd_id);
            //db.pst.setInt(3,Integer.parseInt(tphno.getText()));
            db.pst.setString(4,tphno.getText());
            db.pst.setString(5,temail.getText());
            db.pst.setString(6,tfname.getText());
            db.pst.setInt(7,Integer.parseInt(tage.getText()));
            
            if(rfemale.isSelected())
            db.pst.setString(8,rfemale.getText());
            else if(rmale.isSelected())
            db.pst.setString(8,rmale.getText());
            
             db.pst.setInt(9,Login.seat);
             db.pst.setInt(10,Login.price);
             
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd"); 
            java.util.Date date = sdf.parse(Login.date);
            java.sql.Date sqlDate = new java.sql.Date(date.getTime());
            db.pst.setDate(11,sqlDate);
            
             if(db.pst.executeUpdate() != 0){
                            JOptionPane.showMessageDialog(null, "Ticket booked");
                            new Search().setVisible(true);
                            this.dispose();
                         }else{
                             JOptionPane.showMessageDialog(null, "Error: Check Your Information");
                         }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_bookActionPerformed

